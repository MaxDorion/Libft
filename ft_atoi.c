int	ft_atoi(const char *nptr)
{
	int	i;
	int	j;
	int	k;

	k = 0;
	j = 1;
	i = 0;
	while (nptr[k] == ' ' || (nptr[k] >= 9 && nptr[k] <= 13))
		k++;
	if (nptr[k] == '-' || nptr[k] == '+')
	{
		if (nptr[k] == '-')
			j = j * (-1);
		k++;
	}
	while (nptr[k] >= '0' && nptr[k] <= '9')
	{
		i *= 10;
		i = i + ((int)nptr[k] - 48);
		k++;
	}
	return (i * j);
}
